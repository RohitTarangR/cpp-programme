#include<iostream>
using namespace std;

int main()
{
    int num1=6;
    int num2=3;

    cout<<(num1==num2)<<endl;     //False
    cout<<(num1!=num2)<<endl;     //True    
    cout<<(num1>num2)<<endl;      //True
    cout<<(num1<num2)<<endl;      //False
    cout<<(num1<=num2)<<endl;     //False
    cout<<(num1>=num2)<<endl;     //True


    return 0;
}