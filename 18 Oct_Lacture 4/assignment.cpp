#include<iostream>
using namespace std;

int main ()
{
    int num=2;
    int num1=6;
    int num2=3;
    int num3=4;
    int num4=5;


    num=2;
    cout<<num<<endl;

    num1+=2;    //6+2=8
    cout<<num1<<endl;

    num2-=2;    //3-2=1
    cout<<num2<<endl;

    num3/=2;    //4/2=2
    cout<<num3<<endl;

    num4%=2;    //5%2=1
    cout<<num4<<endl;

    return 0;

}